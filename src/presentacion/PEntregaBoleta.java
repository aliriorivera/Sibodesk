    private void imprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imprimirActionPerformed
        // TODO add your handling code here:
        if (resultados.getSelectedRow()>=0){
            if (resultados.getValueAt(resultados.getSelectedRow(), 4).equals("Comprada")){
                //agregar codigo de impresion y colocar la boleta como entregada, luego actualizar la tabla.
                Boleta boleta=boletaDAO.findBoleta((Integer)resultados.getValueAt(resultados.getSelectedRow(),0));
                if (imprimirBoleta(boleta)){
                    boleta.setEstado("Entregada");
                    try {
                        boletaDAO.edit(boleta);
                    } catch (NonexistentEntityException ex) {
                        Logger.getLogger(PEntregaBoleta.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (Exception ex) {
                        Logger.getLogger(PEntregaBoleta.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    cargarTabla(false);
                }
                else
                    JOptionPane.showMessageDialog(null, "No se pudo imprimir la boleta.");
            }
            else
                JOptionPane.showMessageDialog(null, "Debe registrar el saldo antes de imprimir la boleta para una reserva.");
        }
        else
            JOptionPane.showMessageDialog(null, "Debe seleccionar una boleta");
    }//GEN-LAST:event_imprimirActionPerformed

