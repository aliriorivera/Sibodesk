    private void cargarTabla(boolean mostrarMensaje){
        borrarDatosTabla();
        boolean error=false;
        List<Boleta> boletasWeb=boletaDAO.getBoletaByClienteWeb(documento.getText());
        List<Boleta> boletas=boletaDAO.getBoletaByCliente(documento.getText());
        DefaultTableModel modelo=new DefaultTableModel();
        modelo=(DefaultTableModel)resultados.getModel();
        if (boletas!=null && !boletas.isEmpty()){
            for (int i=0;i<boletas.size();i++)
                modelo.insertRow(modelo.getRowCount(),new Object[]{boletas.get(i).getId(),boletas.get(i).getFkLocacion().getFkPresentacion().getFkEvento().getNombre(),boletas.get(i).getFkLocacion().getNombre(),boletas.get(i).getFkLocacion().getPrecio(),boletas.get(i).getEstado()});
        }
        else
            error=true;
        if (boletasWeb!=null && !boletasWeb.isEmpty()){
            error=false;
            for (int i=0;i<boletasWeb.size();i++)
                modelo.insertRow(modelo.getRowCount(),new Object[]{boletasWeb.get(i).getId(),boletasWeb.get(i).getFkLocacion().getFkPresentacion().getFkEvento().getNombre(),boletasWeb.get(i).getFkLocacion().getNombre(),boletasWeb.get(i).getFkLocacion().getPrecio(),boletasWeb.get(i).getEstado()});
        }
        if (error){
            if (mostrarMensaje)
                JOptionPane.showMessageDialog(null, "No se econtraron boletas con el documento ingresado.");
        }
        else
            resultados.setModel(modelo);
    }

