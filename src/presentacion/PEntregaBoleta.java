    private boolean imprimirBoleta(Boleta boleta){
        boolean print=false;
        PrinterJob job=PrinterJob.getPrinterJob();
        Paper papel=new Paper();
        papel.setSize(200, 700);
        papel.setImageableArea(0, 0, 200, 700);
        PageFormat formato=job.defaultPage();
        formato.setPaper(papel);
        ImprimirBoleta imp=new ImprimirBoleta();
        imp.setBoleta(boleta);
        job.setPrintable(imp,formato);
        print=job.printDialog();
        if (print){
            try {
                job.print();
            } catch (PrinterException ex) {
                Logger.getLogger(PEntregaBoleta.class.getName()).log(Level.SEVERE, null, ex);
                return false;
            }
            if (!job.isCancelled())
                return true;
        }
        return false;
    }

