    private void jButtonBuscarEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarEliminarActionPerformed
        // TODO add your handling code here:
         String lCampoFiltro = null;
        String lValorFiltro = null;
        RegistrarVendedor lRegistrarVendedor;
        posicion = 0;
        this.jButtonEditar.setVisible(false);
        this.jButtonRegistrar.setVisible(true);
        this.jButtonEliminar.setVisible(false);

        try
        {
            if( this.jRadioFiltroCedularEliminar.isSelected() )
            {
                lCampoFiltro = "Cedula";
            }
            else if( this.jRadioNombreEliminar.isSelected() )
            {
                lCampoFiltro = "Nombre";
            }

            lValorFiltro = jTextValorBusquedaEliminar.getText();
            lRegistrarVendedor = new RegistrarVendedor();
            registrosUsuario = lRegistrarVendedor.obtenerVendedores(lCampoFiltro, lValorFiltro);
            if( registrosUsuario == null || registrosUsuario.isEmpty() )
            {
                this.presentarMensaje("No se enctraron registros", "Mensaje de aviso");
            }
            else
            {
                    usuarioActual = ( Usuario ) registrosUsuario.get(posicion);
                    jButtonEliminar.setVisible(true);
                    presentarUsuario(usuarioActual);
                    repaint();
            }
        }
        catch( Exception pException )
        {
             this.presentarMensaje( pException.getMessage(), "Mensaje de aviso");
        }

    }//GEN-LAST:event_jButtonBuscarEliminarActionPerformed

