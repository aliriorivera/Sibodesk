    private void cargarPuntosDeVenta()
    {
        Iterator lIterator = null;
        Puntodeventa lPuntodeVenta = null;
        RegistrarPuntoVenta lRegistrarPuntoVenta = new RegistrarPuntoVenta();
        listaPuntoVenta = lRegistrarPuntoVenta.getPuntosDeVenta();
        if( listaPuntoVenta != null && !listaPuntoVenta.isEmpty() )
        {
            lIterator = listaPuntoVenta.iterator();
            while( lIterator.hasNext() )
            {
               lPuntodeVenta = ( Puntodeventa ) lIterator.next();
               jComboBoxPuntoVenta.addItem(lPuntodeVenta.getNombre());

            }
        }
    }

