    private Usuario obtenerDatos()
    {
        Usuario lUsuario = new Usuario();
        Puntodeventa lPuntodeventa = new Puntodeventa();
        Rol lRol = new Rol();

        lUsuario.setNombres( jTextNombres.getText() );
        lUsuario.setApellidos(jTextApellidos.getText());
        lUsuario.setDireccion( jTextDireccion.getText() );
        lUsuario.setTelefono( jTextTelefono.getText() );
        lUsuario.setCedula( jTextCedula.getText());
        lUsuario.setUsuario(jTextUsuario.getText());
        //if (!jPasswordClave.getText().isEmpty())
            lUsuario.setContrasena(jPasswordClave.getText());
        lUsuario.setActivo(jCheckBoxActivo.isSelected());
        lPuntodeventa.setId( listaPuntoVenta.get(jComboBoxPuntoVenta.getSelectedIndex()).getId());
        lUsuario.setFkPuntodeventa(lPuntodeventa);
        lRol.setId(2);
        lUsuario.setFkRol(lRol);

        return lUsuario;

    }

