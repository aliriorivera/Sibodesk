    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:

        int ans =  JOptionPane.showConfirmDialog((Component)null,"¿Esta seguro de que desea eliminar este punto de venta?", "Confirmacion",JOptionPane.YES_NO_OPTION);
        if (ans == JOptionPane.YES_OPTION) {
                deleteptoventa = new RegistrarPuntoVenta();
                if(identificaciondelete!=0){
                    try {
                        int a = deleteptoventa.deleteptoventa(identificaciondelete);
                        if(a==0){
                             JOptionPane.showMessageDialog(null, "El punto de venta fue borrado exitosamente");
                             pvnombredel.setText("");
                             pvciudaddel.setText("");
                             pvdirecciondel.setText("");
                             pvtelefonodel.setText("");
                             identificaciondelete = 0;
                        }
                        
                    } catch (NonexistentEntityException ex) {
                        Logger.getLogger(PAdminpuntoventa.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog((Component)null,
                                        "hubo un error de conexion a la base de datos", "Error",JOptionPane.ERROR_MESSAGE);
                    }
                }
                else{
                    JOptionPane.showMessageDialog((Component)null,
                                        "No ha seleccionado ningun registro", "Error",JOptionPane.ERROR_MESSAGE);
              }
        }


    }//GEN-LAST:event_jButton6ActionPerformed

