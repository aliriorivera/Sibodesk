            if(ptoventa1.getHorario().equals("L-V 8-5pm S 9-1pm")){
                pvlistaed.setSelectedIndex(1);
            }
            if(ptoventa1.getHorario().equals("L-V 9-5pm")){
                pvlistaed.setSelectedIndex(2);
            }
